        <section class="section-informational">
            <h2>Liquidaciones de sueldo y jornales más comunes</h2>
            <p>Como ya dijimos, es esencial realizar una buena liquidación de sueldos y es importante saber que estás varía  según el tipo de trabajo y rubro, como el tipo de circunstancia. Por eso en esta sección, exploraremos las liquidaciones de sueldos más comunes, proporcionando una guía para distintos sectores y situaciones laborales.</p>
            <p>Contar con una correcta asistencia en esta área es crucial para realizar una correcta retribución de haberes y el cumplimiento correcto de la normativa. </p>
            
            <h3>Liquidación de sueldos empleados de comercio</h3>
            <p>En una liquidación de sueldos para empleados de comercio hay que tener en consideración distintas cuestiones, como el sector, horas trabajadas, bonus, antigüedad y horas extra si es que las hubo. Este sector se encuentra regido por un convenio colectivo que busca dar una correcta remuneración a sus trabajadores y ver que se cumpla la normativa correspondiente.</p>
        
            <h3>Liquidación de sueldos uocra</h3>
            <p>Este sector tiene algunas particularidades diferentes al resto de sectores por su área de trabajo, la construcción.</p>
            <p>En el sector representado por la UOCRA hay puntos adicionales a tener en cuenta, como jornadas laborales en obras, los adicionales por zona y las bonificaciones por riesgo son esenciales en el cálculo.</p>
        
            <h3>Liquidación de sueldo empleada doméstica</h3>
            <p>Este sector es uno de los más comunes y no cuenta con grandes variantes como los anteriores, aquí los aspectos más importantes son el pago por horas, los beneficios sociales y los derechos laborales específicos del sector. La liquidación de sueldo para empleada doméstica, puede parecer algo sencillo pero no por eso menos importante que los demás.</p>

            <h3>Liquidación de sueldo por renuncia</h3>
            <p>Cuando un empleado renuncia se tiene que realizar su liquidación correspondiente. Esta liquidación debe incluir todos los pagos pendientes hasta la finalización del contrato. Este pago incluye</p>
            <ul>
                <li>- salario proporcional</li>
                <li>- vacaciones no gozadas </li>
                <li>- aguinaldo proporcional</li>
            </ul>
            <p>Es importante como empleado conocer esto para evitar liquidaciones incorrectas y que no paguen lo correspondiente.</p>
        
            <h3>Liquidación de sueldo por despido</h3>
            <p>Una liquidación de sueldo por despido implica unas compensaciones extra (a comparación de una renuncia) y estas varían según las condiciones del despido. Este proceso incluye lo mismo que una liquidación por renuncia, pero además hay que agregarle el pago de indemnizaciones, que varía según antigüedad, motivo de despido, entre otros aspectos a tener en cuenta.</p>
        </section>
        
