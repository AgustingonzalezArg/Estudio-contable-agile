            <h2>Como se hace una liquidación de sueldo</h2>
            <p>Una liquidación lo más común es que la realice un contador o estudio contable, pero es importante conocer el proceso para poder conocer de lo que se habla.</p>
            <h3>Haberes imponibles</h3>
            <p>Son los ingresos que recibe el trabajador a cambio de su tiempo trabajado. Estos pueden ser sueldos, horas extra, bonos y comisiones.</p>
            <h3>Haberes no imponibles</h3>
            <p>Son los ingresos que recibe el trabajador a cambio de su tiempo trabajado. Estos pueden ser sueldos, horas extra, bonos y comisiones.</p>
            <h3>Carga impositiva</h3>
            <p>Además de lo nombrado anteriormente hay que tener en cuenta los impuestos correspondientes a pagar por tener a ese empleado trabajando en la empresa.</p>
