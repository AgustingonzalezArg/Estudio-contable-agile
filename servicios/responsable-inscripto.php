                <h2>Responsables inscriptos y el iva</h2>
                <p>El IVA (Impuesto al Valor Agregado) es un impuesto indirecto que grava el consumo de productos y servicios, afectando al consumidor final. En Argentina, las tasas de IVA son del 21% para la mayoría de los bienes y servicios, del 10,5% para bienes y servicios esenciales, y del 27% para servicios de telecomunicaciones, energía eléctrica y gas en ciertas situaciones.</p>
                <p>Los Responsables Inscriptos deben emitir facturas donde se discrimine el IVA, y estos montos pueden ser utilizados como crédito fiscal contra el débito fiscal de sus ventas. Esto permite a los negocios deducir el IVA que han pagado en sus compras del IVA que cobran en sus ventas, ayudando a reducir su carga tributaria (Tener "IVA a favor" que comentabamos anteriormente).</p>
