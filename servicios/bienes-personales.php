            <h3>¿Qué es una declaración jurada informativa de bienes personales?</h3>
            <p>La declaración jurada es un documento que cada persona debe presentar ante la AFIP en donde declara sus bienes y activos, y presentas ante el organismo cuanto es el saldo a pagar. Estos bienes incluyen automóviles, casas o propiedades, terrenos, cuentas bancarias, inversiones, entre otras cosas. </p>
            <h3>¿Cuándo debo presentar a afip la declaración de bienes personales?</h3>
            <p></p>
            <h3>¿Qué pasa si no realizo mi declaración de bienes personales?</h3>
            <p>El no presentar la declaración ante la AFIP sobre tus bienes personales no es una gran idea, ya que te podrían caer multas e intereses por parte de la AFIP y además tener sanciones fiscales.</p>
            <h3>¿Cómo realizo mi declaración jurada informativa de bienes personales?</h3>
            <p>Para realizar tu declaración jurada informativa ante la AFIP tendrías que seguir los siguientes pasos:</p>
            <ul>
                <li>Reunir toda la información correspondiente que habría que presentar.</li>
                <li>Ingresar al sitio web de la AFIP con tu clave fiscal y completar el formulario correspondiente con toda la información obtenida previamente</li>
                <li>Una vez completado el formulario deberías corroborar que este todo correcto, y si es así, lo envía la declaración mediante la página de AFIP.</li>
            </ul>
