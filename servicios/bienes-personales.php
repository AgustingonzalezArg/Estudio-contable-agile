                <h2>¿Qué es una liquidación de bienes personales?</h2>
                <p>La liquidación de bienes personales vendría a ser el monto o cálculo realizado para saber de cuanto es el patrimonio y de esta forma saber cuanto es lo que tengo que pagar de impuesto sobre bienes personales.</p>
                <p>Calcular esto lleva un proceso que debería realizar un especialista pero en grandes rasgos se debería sumar todos los bienes, tanto en el país como en el exterior y luego hacer las deducciones legales permitidas, como deudas o créditos fiscales. Una vez se obtiene el número se calcula cuanto es lo correspondiente a pagar según el monto.</p>
                <h3>¿Qué pasa si no realizo mi liquidación de bienes personales?</h3>
                <p>El no realizar la liquidación hace que no sea posible presentar la declaración jurada informativa ante la AFIP, lo que trae consigo los problemas mencionados anteriormente. Multas, restricciones fiscales, entre otras consecuencias</p>
