            <ul>
                <li>Accede al sitio web de AFIP utilizando una clave fiscal de nivel 2 o superior. Busca el servicio titulado "Devolución de percepciones". Si no está disponible, puedes activarlo desde la sección "Mis Servicios".</li>
                <li>Dentro del servicio "Devolución de percepciones", elige la opción "Nuevo".</li>
                <li>Especifica el período fiscal correspondiente para el cual deseas solicitar la devolución.</li>
                <li>A continuación, podrás ver y seleccionar las percepciones que se registraron durante ese período y que han sido informadas por el agente de percepción. Si no aparecen, tienes la opción de ingresarlas manualmente.</li>
                <li>Finalmente, haz clic en "Presentar".</li>
            </ul>
