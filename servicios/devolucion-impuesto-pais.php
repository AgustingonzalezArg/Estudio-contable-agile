        <section class="section-informational">
            <h2> ¿Cuánto demora en acreditarse las devoluciones?</h2>
            <p>Una vez enviada y aprobada la solicitud del reintegro de percepciones, el trámite puede llegar a demorar hasta 60 días.</p>
            <p>Ahora <strong>¿qué sucede si no recibo la devolución?</strong></p>
            <p>En la misma página de AFIP te permiten hacer el seguimiento de las devoluciones solicitadas. Para ello debes entrar al portal e ingresar con la clave fiscal y la contraseña. Luego deberás ir a Devolución de Percepciones y especificar que ya fue presentada la solicitud y seleccionar la opción de seguimiento en la barra superior de la izquierda. En este lugar vas a poder ver en qué estado se encuentra tu solicitud.</p>
            <p>En el caso que se haya impedido la devolución te van a especificar el motivo de la misma. Estos son los puntos por los cuales te lo pueden haber rechazado.</p>
            <ul>
                <li><strong>CBU no declarado</strong>: Para resolver esto, debes acceder a la opción "Declaración de CBU" y registrar la cuenta bancaria donde deseas recibir la devolución. Es fundamental que sea una cuenta bancaria y no una Cuenta Virtual Única (CVU).</li>
                <li><strong>CUIT inactiva</strong>: Aquellos usuarios que no posean CUIT, lo tengan inhabilitado o desactivado, deberán descargar la aplicación "Mi AFIP". Una vez instalada, deberán acceder a la opción "ALTA CUIT" y seguir los pasos para completar el proceso.</li>
                <li><strong>Deuda con AFIP</strong>: En esta situación, deberás ir a la sección "Estado de cuenta". Luego, selecciona "Ver saldo" y dirígete a "Cálculo de deuda" para encontrar el pago pendiente con el ente regulador. Una vez pagada la deuda, las percepciones ya deberían estar habilitadas para ser devueltas.</li>
                <li><strong>Formulario sujeto a fiscalización</strong>: Esto sucede cuando las percepciones se ingresan manualmente, (a veces también sucede con las cargadas automáticamente) lo que requiere que el organismo las verifique, prolongando así el proceso. Es decir, la solicitud está en revisión manual. En estos casos, se recomienda consultar con un contador o dirigirse personalmente a las oficinas de AFIP.</li>
            </ul>
