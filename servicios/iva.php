            <article id="iva-que-es">
                <h2>IVA: ¿Qué es?</h2>
                <br>
                <h3 id="definicion-iva">Impuesto al Valor Agregado: Definición</h3>
                <p>El IVA (Impuesto al Valor Agregado) es una carga impositiva que recae sobre el consumo, financiada por el consumidor final a través del precio de los productos y servicios que adquiere. Este impuesto es recaudado por los comerciantes y responsables inscriptos, quienes deben discriminarlo en sus ventas y luego declararlo ante la AFIP para cumplir con sus obligaciones tributarias.</p>
                <h3 id="para-que-sirve-iva">¿Para qué sirve el IVA?</h3>
                <p>El impuesto de valor agregado es, en resumen, un mecanismo del gobierno para recaudar impuestos y dinero a través del consumo de las personas. Algunos lo ven bien otros no tanto, sobre todo en cosas tan esenciales como alimentos que aunque suelen tener una tasa más baja, dicen que deberían no tener directamente.</p>
            </article>
            <article id="aspectos-generales-iva">
                <h2>Aspectos Generales del IVA</h2>
                <br>
                <h3 id="vs-impuesto-valor-anadido">¿Impuesto de Valor Agregado vs. Impuesto Valor Añadido?</h3>
                <p>Una confusión a aclarar es que muchas personas piensan que el Impuesto de Valor Agregado y el Impuesto Valor Añadido son distintas cosas cuando no es así. Estas dos palabras son sinónimos y se utilizan para el mismo contexto</p>
                <h3 id="diferentes-contextos-iva">El IVA en diferentes contextos</h3>
                <p>El iva y su precio o porcentaje va a variar dependiendo diferentes contextos porque no todos los productos tienen la misma tasa de impuesto al valor añadido. Existen tres tipos de alícuotas: 10,5%, 21% y 27%.</p>
                <h4 id="iva-alimentos">IVA en alimentos</h4>
                <p>En alimentos tenemos dos categorías para agrupar. El grupo que sería el grupo de comida más básico tiene un IVA del 10.5%, con una carga final tributaria del casi 36,2%. Ahora el segundo cuenta con un IVA mayor llegando al 21% que serían comidas como arroz, pan, fideos, aceite y cuentan con una carga tributaria final cerca del 41,8%</p>
                <h4 id="iva-servicios-productos">IVA en servicios y productos</h4>
                <p>Esta es la categoría más amplia y abarca la mayoría de bienes y comercios. En estos casos la carga del IVA se encuentra en el 21%.</p>
                <h4 id="iva-electricos-telecomunicaciones">IVA en servicios eléctricos y telecomunicaciones</h4>
                <p>En este caso la carga del impuesto al valor agregado es del 27% porque consideran que las personas que contratan estos servicios tienen “Más recursos”.</p>
                <h3 id="exento-iva">Exento de IVA: ¿Qué significa y para qué sirve?</h3>
                <p>Que un producto esté exento de IVA significa que en situación normal este producto debería tener una carga tributaria de IVA  pero por disposición del Gobierno ese producto no tendrá la carga tributaria, para de esta forma alivianar la carga impositiva y por ende reducir el precio del mismo.</p>
