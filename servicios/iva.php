            <article id="regulaciones-normativas-iva">
                <h2>Regulaciones y Normativas del IVA</h2>
                <p>El Impuesto al valor añadido tiene varias regulaciones y normativas que deberías conocer. Si quieres más información puedes visitar las páginas de AFIP haciendo clic en cada una de ellas.</p>
                <h3 id="iva-rg-4240">IVA RG 4240</h3>
                <p>La Resolución General N° 4240/2018 dice que, si compras servicios digitales a empresas del exterior y no eres un responsable inscripto en IVA, debes seguir ciertas reglas. Si pagas a través de una empresa en tu país que maneja estos pagos, esa empresa se encargará de cobrar y liquidar el impuesto. Si pagas directamente a la empresa del exterior y no usas una empresa local para hacerlo, tú mismo tendrás que pagar el impuesto hasta el último día del mes en que hiciste el pago, usando un formulario electrónico.</p>
                <h3 id="iva-rg-3337">IVA RG 3337</h3>
                <p>La RG 3337 busca mejorar el control y la transparencia en las transacciones internacionales, asegurando que los contribuyentes cumplan con sus obligaciones fiscales y reporten adecuadamente las operaciones que puedan tener impacto en la recaudación del IVA. La resolución establece requisitos específicos de información y documentación para las operaciones que involucren a entidades o personas fuera del país, con el fin de prevenir la evasión fiscal y garantizar el correcto cumplimiento de las normativas vigentes.</p>
                <h3 id="iva-rg-2408">IVA RG 2408</h3>
                <p>La Resolución General N° 2408/2008 establece que cuando compras bienes o servicios del exterior y no eres un responsable inscripto en IVA, tienes que seguir ciertos pasos. Si pagas a través de una empresa en tu país que gestiona estos pagos, esa empresa se encargará de cobrar y pagar el IVA. Sin embargo, si pagas directamente a la empresa del exterior sin intermediarios locales, tendrás que abonar el IVA tú mismo y hacerlo hasta el último día del mes en que hiciste el pago, usando un formulario electrónico. Aunque parezca igual que la  RG 4240, esta es más reciente y detalla cómo deben actuar los intermediarios en pagos a proveedores del exterior, y qué hacer si no hay intermediarios.</p>
                <h3 id="iva-rg-2300">IVA RG 2300/2007</h3>
                <p>La Resolución General N° 2300/2007 establece que, si eres un comprador en Argentina y adquieres bienes o servicios del exterior, debes tener en cuenta cómo se maneja el IVA. Si compras directamente a proveedores extranjeros sin usar intermediarios locales, eres responsable de pagar el IVA correspondiente tú mismo. Debes hacerlo mediante un formulario electrónico, que debes presentar antes del final del mes en que realizaste la compra. Esta normativa asegura que el IVA se pague correctamente en estas transacciones internacionales.</p>
            </article>
            <article id="declaraciones-juradas-iva">
                <h2>Declaraciones Juradas de IVA</h2>
                <p>Las declaraciones juradas de IVA son un documento que deben presentar los responsables inscriptos ante la AFIP para mediante su página web determinar el pago de IVA. </p>
                <h3 id="declaracion-iva-linea">Declaración del IVA en línea</h3>
                <p>Las declaraciones de IVA se realizan de forma mensual y en línea a través de la página de AFIP generando los libros de IVA digital para luego verificar cuánto debería pagar la empresa. Este es un proceso que suelen llevar contadores o especialistas en el tema, ya que requiere cierto grado de conocimiento y expertis. Pero si te interesa gestionarlo por tu cuenta <a href="https://serviciosweb.afip.gob.ar/genericos/guiasPasoPaso/VerGuia.aspx?id=98">aquí </a>tienes una página de AFIP que indica el paso a paso</p>
                <h3 id="declaracion-complementaria-iva">¿Qué es una declaración complementaria de IVA?</h3>
                <p>Una declaración complementaria es una forma de rectificar o corregir errores en una declaración de IVA realizada y presentada previamente. Esto puede suceder por algún error de cálculo a la hora de realizarla o por omisión de información correspondiente.</p>
            </article>
            <article id="liquidacion-calculo-iva">
                <h2>Liquidación y Cálculo del IVA</h2> 
                <br>
                <h3 id="liquidacion-iva">¿Qué es una liquidación de IVA y como calcular cuanto hay que pagar?</h3>
                <p>Una liquidación de IVA es el cálculo que se realiza para determinar cuánto hay que pagar del Impuesto al valor agregado. Este es un proceso sencillo y aquí esta la formula: </p>
                <p>Debito Fiscal - Crédito Fiscal = IVA total a pagar</p>
                <p>Hay muy pocos casos en donde el resultado queda a favor del contribuyente. Esto puede suceder cuando uno recién está empezando y tiene que comprar stock</p>
                <h3 id="debito-fiscal-iva">IVA débito fiscal: ¿Qué es?</h3>  
                <p>El débito fiscal es el IVA recaudado por una empresa mediante la venta de sus productos o servicios y es lo que se le debería pagar a la AFIP</p>
                <h3 id="credito-fiscal-iva">IVA crédito fiscal: ¿Qué es?</h3>
                <p>El crédito fiscal es el IVA a favor que tiene una empresa mediante sus compras realizadas a sus proveedores y esto es le debería quedar a favor en AFIP para la empresa.</p>
